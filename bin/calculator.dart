
  
int add(int a, int b) {
  int result = a + b;
  return result;
}
int sub(int a, int b) {
  int result = a - b;
  return result;
}
int mul(int a, int b) {
  int result = a * b;
  return result;
}

double div(double a, double b) {
  double result = a / b;
  return result;
}
int rem(int a, int b) {
  int result = a % b;
  return result;
}
